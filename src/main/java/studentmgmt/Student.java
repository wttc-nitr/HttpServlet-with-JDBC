package studentmgmt;

public class Student {
    private int id;
    private String name;
    private String phone;
    private String city;

    public String getName() {
        return this.name;
    }

    public String getPhone() {
        return this.phone;
    }

    public String getCity() {
        return this.city;
    }

    public int getId() {
        return this.id;
    }
}